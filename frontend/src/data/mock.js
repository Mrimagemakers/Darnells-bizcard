// Mock saved gallery data (stored in localStorage)
export const getSavedArtworks = () => {
  const saved = localStorage.getItem('coloringArtworks');
  return saved ? JSON.parse(saved) : [];
};

export const saveArtwork = (artwork) => {
  const artworks = getSavedArtworks();
  artworks.push({
    ...artwork,
    id: Date.now().toString(),
    timestamp: new Date().toISOString()
  });
  localStorage.setItem('coloringArtworks', JSON.stringify(artworks));
};

export const deleteArtwork = (id) => {
  const artworks = getSavedArtworks();
  const filtered = artworks.filter(art => art.id !== id);
  localStorage.setItem('coloringArtworks', JSON.stringify(filtered));
};
